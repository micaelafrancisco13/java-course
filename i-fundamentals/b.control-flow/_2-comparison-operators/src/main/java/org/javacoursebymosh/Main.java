package org.javacoursebymosh;

public class Main {
    public static void main(String[] args) {
        // comparison operators are used to compare
        // primitive values
        int x = 1;
        int y = 1;

        // boolean expressions
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x > y);
        System.out.println(x < y);

        System.out.println("Hello world!");
    }
}